def get_algorithm(case_id: str) -> str:
    """
    Récupère l'algorithme pour un cas donné
    
    Args:
        case_id: Identifiant du cas (ex: "T-BBFF-AsA")
    
    Returns:
        L'algorithme correspondant ou chaîne vide si non trouvé
    """
    parts = case_id.split('-')
    if len(parts) >= 3:
        family = parts[0]
        subcase = parts[1]
        
        if (family in ZBLL_ALGORITHMS and 
            subcase in ZBLL_ALGORITHMS[family] and 
            case_id in ZBLL_ALGORITHMS[family][subcase]):
            return ZBLL_ALGORITHMS[family][subcase][case_id]["algorithm"]
    
    return ""

def set_algorithm(case_id: str, algorithm: str):
    """
    Définit l'algorithme pour un cas donné
    
    Args:
        case_id: Identifiant du cas (ex: "T-BBFF-AsA")
        algorithm: L'algorithme à associer
    """
    parts = case_id.split('-')
    if len(parts) >= 3:
        family = parts[0]
        subcase = parts[1]
        
        if (family in ZBLL_ALGORITHMS and 
            subcase in ZBLL_ALGORITHMS[family]):
            if case_id not in ZBLL_ALGORITHMS[family][subcase]:
                ZBLL_ALGORITHMS[family][subcase][case_id] = {"name": "", "algorithm": ""}
            ZBLL_ALGORITHMS[family][subcase][case_id]["algorithm"] = algorithm

def get_all_algorithms() -> dict:
    """Retourne toute la base de données"""
    return ZBLL_ALGORITHMS

