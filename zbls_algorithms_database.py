def get_algorithm(case_name: str) -> str:
    """
    Récupère l'algorithme pour un cas ZBLS donné
    
    Args:
        case_name: Nom du cas (ex: "F2L 1-1")
    
    Returns:
        L'algorithme associé ou chaîne vide si non trouvé
    """
    parts = case_name.split('-')
    if len(parts) == 2:
        family = f"F2L {parts[0]}"
        case_num = parts[1]
        full_case_name = f"{family}-{case_num}"
        
        if family in ZBLS_ALGORITHMS:
            for case_key, case_data in ZBLS_ALGORITHMS[family].items():
                if case_data["name"] == full_case_name:
                    return case_data["algorithm"]
    return ""

def set_algorithm(case_name: str, algorithm: str) -> None:
    """
    Définit l'algorithme pour un cas ZBLS donné
    
    Args:
        case_name: Nom du cas (ex: "F2L 1-1")
        algorithm: L'algorithme à associer
    """
    parts = case_name.split('-')
    if len(parts) == 2:
        family = f"F2L {parts[0]}"
        case_num = parts[1]
        full_case_name = f"{family}-{case_num}"
        
        if family in ZBLS_ALGORITHMS:
            for case_key, case_data in ZBLS_ALGORITHMS[family].items():
                if case_data["name"] == full_case_name:
                    case_data["algorithm"] = algorithm
                    break

def get_family_cases(family: str) -> dict:
    """
    Récupère tous les cas d'une famille ZBLS
    
    Args:
        family: Nom de la famille (ex: "F2L 1")
    
    Returns:
        Dictionnaire des cas de la famille
    """
    return ZBLS_ALGORITHMS.get(family, {})

def get_category_cases(category: str) -> dict:
    """
    Récupère toutes les familles d'une catégorie
    
    Args:
        category: Nom de la catégorie (ex: "F2L 1-10")
    
    Returns:
        Dictionnaire des familles de la catégorie
    """
    return ZBLS_CATEGORIES.get(category, {})

def get_all_algorithms() -> dict:
    """Retourne toute la base de données ZBLS"""
    return ZBLS_ALGORITHMS

def get_all_categories() -> dict:
    """Retourne toutes les catégories ZBLS"""
    return ZBLS_CATEGORIES

def get_stats() -> dict:
    """Retourne les statistiques ZBLS"""
    return ZBLS_STATS

def save_to_file(filename: str = "zbls_algorithms_data.json") -> None:
    """
    Sauvegarde la base de données dans un fichier JSON
    
    Args:
        filename: Nom du fichier de sauvegarde
    """
    import json
    
    data = {
        "algorithms": ZBLS_ALGORITHMS,
        "categories": ZBLS_CATEGORIES,
        "stats": ZBLS_STATS
    }
    
    with open(filename, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def load_from_file(filename: str = "zbls_algorithms_data.json") -> None:
    """
    Charge la base de données depuis un fichier JSON
    
    Args:
        filename: Nom du fichier à charger
    """
    import json
    
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            data = json.load(f)
            
        global ZBLS_ALGORITHMS, ZBLS_CATEGORIES, ZBLS_STATS
        ZBLS_ALGORITHMS = data.get("algorithms", {})
        ZBLS_CATEGORIES = data.get("categories", {})
        ZBLS_STATS = data.get("stats", {})
    except FileNotFoundError:
        print(f"Fichier {filename} non trouvé. Utilisation de la base par défaut.")

